export function hasElementData(element: string): boolean {
	return element in covalentRadii && element in vdwRadii && element in elementColors;
}

export function covalentRadius(element: string): number {
	return covalentRadii[element] ?? fallbackCovalentRadius;
}

export function vdwRadius(element: string): number {
	return vdwRadii[element] ?? fallbackVdwRadius;
}

export function elementColor(element: string): number {
	return elementColors[element] ?? fallbackColor;
}

export function atomRenderRadius(element: string): number {
	if (element === "H" || element === "D") return 0.16;
	return clamp(covalentRadius(element) * 0.22 + 0.05, 0.2, 0.34);
}

export function atomPickRadius(element: string): number {
	if (element === "H" || element === "D") return 0.42;
	return clamp(covalentRadius(element) * 0.22 + 0.36, 0.5, 0.72);
}

function clamp(value: number, min: number, max: number): number {
	return Math.min(Math.max(value, min), max);
}
